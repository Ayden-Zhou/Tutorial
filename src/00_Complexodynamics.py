# 在回答 Sean 的问题（即是否存在某种“复杂动力学定律”可以解释他给出的图表）时，挑战似乎是双重的：
# 
# 1.  提出一个合理的“复杂性”形式化定义。
# 2.  证明在该定义下，自然模型系统在中间时刻的“复杂性”很大，尽管在初始时刻和演化后期都接近于零。
# 
# 需要澄清的是：至少在直观推断层面，解释为什么初始时刻的复杂性接近于零并不困难。这是因为假设了熵接近于零，而熵理所当然地为复杂性提供了上限。解释为什么演化后期的复杂性接近于零也不难：因为系统达到了平衡态（即类似于所有可能状态下的均匀分布），这在本质上被定义为简单的。在中间时刻，这两个约束都不再起作用，因此复杂性 **可能** 变得很大。但它 **真的** 变大了吗？有多大？如何预测？以及，我们讨论的究竟是哪种“复杂性”？
# 
# 在断断续续地思考了这些问题后，目前的推测是，可以利用柯尔莫哥洛夫复杂度（Kolmogorov complexity）理论中一个名为 **精致度（sophistication）** 的概念来回答这些问题。回想一下，字符串 $x$ 的柯尔莫哥洛夫复杂度是指输出 $x$ 的最短计算机程序的长度（在某种图灵完备的编程语言中——可以证明具体的语言选择并不重要）。而“精致度”是一个更……嗯，更加精致（sophisticated）的概念，稍后会详细探讨。

# 作为第一步，尝试使用柯尔莫哥洛夫复杂度来定义**熵**。这在直觉上并非显而易见。如果启动一个元胞自动机或一个台球系统，给它一个简单的初始配置，然后让它根据动力学定律演化一段时间，视觉上熵似乎在增加。但如果系统是**确定性**的，那么从数学上讲，它的状态始终可以通过以下信息来指定：（1）初始状态，以及（2）运行的步数 $t$。前者需要固定比特数（与 $t$ 无关），而后者仅需 $\log(t)$ 比特。由此推导，如果使用柯尔莫哥洛夫复杂度作为熵的替代量，那么熵随 $t$ 的增长最多呈**对数级**——这远慢于直觉预期的线性或多项式增长。
# 
# 解决这一问题至少有两种途径。第一种是考虑概率系统而非确定性系统。在概率情况下，柯尔莫哥洛夫复杂度确实会如预期般以多项式速率增长。第二种方案是用**资源受限的柯尔莫哥洛夫复杂度**来代替：即在**较短时间内**输出该状态的最短程序长度（或者，例如输出该状态的最小深度为 3 的电路的大小——就目前而言，施加何种资源限制并不重要，只要限制足够严苛即可）。尽管存在一个仅有 $\log(t)$ 比特长且能计算系统在 $t$ 个时间步后状态的程序，但该程序通常耗费随 $t$ 增长（甚至更快）的时间，因此如果排除掉足够复杂的程序，程序规模将再次随 $t$ 以多项式速率增加。
# 
# 
# 
# 以上是关于熵的讨论。那么 Sean 所称的“复杂性”——为避免混淆，此后称之为 **“复熵”（complextropy）** ——又该如何定义？为此，需要引入一系列相关概念，如精致度（sophistication）、柯尔莫哥洛夫结构函数（Kolmogorov structure functions）以及算法统计学（algorithmic statistics）。背景是，在 20 世纪 70 年代（引入柯尔莫哥洛夫复杂度之后），柯尔莫哥洛夫做出了一个与 Sean 的观察密切相关的发现。他指出，一个均匀随机的字符串具有接近最大值的柯尔莫哥洛夫复杂度，但它同时也是能想象到的 **最不** “复杂”或“有趣”的字符串之一。毕竟，只需通过“它是随机的”这一描述，就能概括关于该字符串几乎所有值得关注的信息。但是，是否存在一种方法能将这种直觉形式化？答案是肯定的。

# 首先，给定一个包含 $n$ 位字符串的集合 $S$，令 $K(S)$ 为输出 $S$ 中所有元素并停机的最短程序比特数。同时，给定集合 $S$ 及其中的元素 $x$，令 $K(x|S)$ 为在拥有“测试是否属于 $S$”的预言机（oracle）的情况下，输出 $x$ 的最短程序长度。那么，可以将 $x$ 的**精致度（Sophistication）**，即 $\text{Soph}(x)$，定义为在满足以下所有条件的集合 $S$ 中，$K(S)$ 可能的最小值：
# 
# 1. $x \in S$
# 2. $K(x|S) \ge \log_2(|S|) - c$ （其中 $c$ 为常数）。
# 
# （换句话说，仅通过说明 $x$ 属于 $S$，就可以提取出 $x$ 中所有的“非随机”信息。）
# 
# 直觉上，$\text{Soph}(x)$ 是描述一个集合 $S$ 的最短程序长度，而 $x$ 是该集合的一个“随机”或“平庸”成员（不一定直接描述 $x$ 本身）。举例来说，任何具有低柯尔莫哥洛夫复杂度的字符串 $x$ 都有较低的精致度，因为我们可以令 $S$ 为单元素集合 $\{x\}$。然而，一个均匀随机的字符串**同样**具有较低的精致度，因为我们可以令 $S$ 为包含所有 $n$ 位字符串的集合 $\{0,1\}^n$。事实上，这引出了一个问题：是否存在**任何**具有高精致度的字符串？显然，在柯尔莫哥洛夫于 20 世纪 80 年代初提出这一问题后，Alexander Shen 给出了肯定的回答（详见 Gács、Tromp 和 Vitányi 的[相关论文](https://www.google.com/search?q=Gacs+Tromp+Vitanyi+algorithmic+statistics+paper)）。其构造过程涉及一个稍显复杂、难以在本篇笔记中展开的对角线论证。
# 
# 但这与咖啡杯有什么关系？乍看之下，精致度似乎具备了我们寻找“复熵（complextropy）”度量时所期望的所有属性：它对简单字符串和均匀随机字符串来说都很小，但对处于“既不简单也不随机”的奇特第三类状态的字符串来说很大。不幸的是，按照上述定义，精致度仍然无法胜任。对于确定性系统，问题与之前指出的柯尔莫哥洛夫复杂度一致：我们始终可以通过指定初始状态、演化规则和 $t$ 来描述系统在 $t$ 步后的状态。因此，精致度永远不会超过 $\log(t) + c$。即使对于概率系统，我们也可以通过指定初始状态、概率演化规则和 $t$ 来指定 $t$ 步后所有可能状态的集合 $S(t)$。并且，至少在假设 $S(t)$ 上的概率分布是均匀的情况下，通过简单的计数论证可知，$t$ 步后的状态几乎总是 $S(t)$ 的一个“平庸”元素。所以，精致度同样几乎永远不会超过 $\log(t) + c$。（如果 $S(t)$ 上的分布是非均匀的，则需要进一步的技术论证，此处略去。）

# 该如何解决这个问题？关键在于引入**计算资源限制（Computational resource bounds）**。（此前在探讨熵的定义时已现端倪。）具体而言，假设将一个 $n$ 位字符串 $x$ 的“复熵（complextropy）”定义如下：
# 
# > 在 $n \log(n)$ 时间内运行，且能输出集合 $S$ 中近乎均匀样本的最短计算机程序的比特数，且满足：(i) $x \in S$，(ii) 在给定可提供 $S$ 中独立均匀样本的预言机（oracle）的情况下，任何能在 $n \log(n)$ 时间内输出 $x$ 的计算机程序，其长度至少为 $\log_2(|S|) - c$（$c$ 为常数）。
# 
# 这里的 $n \log(n)$ 仅作为复杂度界限的一个具体示例：可以用其他时间界限，或限制为常数深度电路等其他弱计算模型来替换。该定义的初衷是希望找到某种“复熵”度量，能让图中第一和第三个咖啡杯的取值接近 0，而为第二个咖啡杯赋予较大的值。因此，我们考虑的是能够输出某种概率分布 $D$ 样本的最短高效程序长度，且满足 $x$ 相对于 $D$ 而言无法被高效压缩。（换句话说，对于任何高效算法， $x$ 看起来都像是来自 $D$ 的“随机”或“平庸”样本。）
# 
# 需要注意的是，在该定义中，在**两个层面**施加计算效率限制至关重要：一是针对采样算法，二是针对在给定采样预言机的情况下重构 $x$ 的算法。如果没有第一个效率约束，根据前文论证，复熵永远不会超过 $\log(t) + c$。同时，如果没有第二个效率约束，复熵虽然会增加，但随后可能会持续增加，原因如下：受时间限制的采样算法无法采样到**完全精确**的集合 $S$，只能得到一个合理的近似；而拥有 **无限时间** 的重构算法则可能利用目标字符串 $x$ 的特殊性质，以少于 $\log_2(|S|) - c$ 的比特数将其重构。
# 
# 但只要对这两种算法都施加计算效率要求，目前的 **猜想** 是：复熵将满足“复杂动力学第一定律”，表现出 Sean 所期望的行为——初始状态小，中间状态大，混合完成后再次变小。目前尚不知如何证明这一猜想。但关键在于，这**并非** 一个为了展示思辩广度而抛出的无解问题，而是一个相对明确的、可以证明实际定理并发表学术论文的问题。
# 
# 若要着手研究，第一步是将上述论述在特定的模型系统和资源约束下进行“实例化”。一个理想的选择是离散化的“咖啡杯”：由黑白像素（“咖啡”和“牛奶”）组成的二维阵列，初始时处于分离状态，随后进行随机临近混合。能否证明对于此类系统，复熵在中间时刻会变大（直觉上，是因为需要描述纯黑区域、纯白区域以及黑白混合区域之间不规则的**边界**）？
# 
# 这种论断既可以从理论上也可以从经验上尝试证明。理论上，尽管有明确的直觉认为该论断成立，但尚无从下手；姑且将其视为一个绝佳的开放性问题。在经验层面，可以尝试在离散咖啡杯等模拟系统中绘制复熵曲线，观察其是否符合预期的“小-大-小”行为。显然，这里的一个难点在于，在上述任何定义下，复熵几乎肯定是不可计算的，甚至难以近似。然而，可以借鉴柯尔莫哥洛夫复杂度相关经验研究的做法：即使用 **可计算** 的量（如 gzip 压缩文件的大小）作为**不可计算** 量（如 $K(x)$）的粗略替代。目前，麻省理工学院的一位优秀本科生 Lauren Oullette 已开始与作者合作进行此类研究。希望到本学期末，能至少在物理学的严谨程度上回答 Sean 的问题。至于达到数学或计算机科学层面的严谨，可能还需要更长的时间。
