# # 复杂动力学第一定律（The First Law of Complexodynamics）
# 
# 在 FQXi 举办的 “Setting Time Aright” 研讨会上，物理学家 Sean Carroll 提出了一个极具美感的问题：为什么物理系统的“复杂性”（complexity）或“趣味性”（interestingness）似乎总是随着时间推移先增加、达到最大值后再减少，而“熵”（entropy）却总是单调增加？
# 
# Scott Aaronson 写下这篇Blog，试图利用 **柯尔莫哥洛夫复杂度（Kolmogorov complexity）** 的概念，为 Sean 的问题勾勒出一个可能的答案。
# 
# 首先是一点背景知识：我们都知道热力学第二定律，它指出任何封闭系统的熵往往会随时间增加，直到达到最大值。这里的“熵”很难定义（我们稍后会再谈到），但它大致衡量了一个系统的“随机性”、“平庸性”或“无序性”。正如 Sean 在他那本精彩的著作《从永恒到此刻》（From Eternity to Here）中指出的，第二定律几乎是一个同义反复（tautology）：一个系统怎么可能不倾向于演化成更“平庸”的配置呢？如果它不这样做，那些配置就不叫平庸了！
# 
# 因此，真正的问题不在于为什么熵在增加，而在于为什么它最初是低的。换句话说，为什么宇宙在大爆炸时的初始状态包含了如此多的“秩序”，以至于宇宙随后的演化过程都在不断地破坏它？我不打算在这篇文章中讨论这个著名的谜团，而只是将初始状态的低熵视为一个既定事实。
# 
# 我们要关注的点是：即使孤立的物理系统在熵值上单调增加，它们在 **“复杂程度”或“趣味性”** 上却并非单调增加。Sean 在这里并没有定义他所说的“复杂”或“有趣”具体指什么——事实上，定义这些概念本身就是他提出的挑战的一部分——但他用咖啡杯的例子说明了他的想法：
