# ### 复杂性的演化模式
# 
# 熵从左至右单调增加，但在直觉上，“复杂性”在中间状态达到顶峰：即那些充满了牛奶触角（tendrils of milk）的画面。
# 
# 同样的规律也适用于整个宇宙：
# * **大爆炸后不久：** 宇宙本质上只是一锅低熵的高能粒子汤。
# * **$10^{100}$ 年后（A googol years from now）：** 在最后的黑洞通过霍金辐射（Hawking radiation）迸发并蒸发殆尽后，宇宙本质上将只是一锅高熵的低能粒子汤。
# * **当下（中间阶段）：** 在这两个极端之间，宇宙包含了有趣的结构，如星系、大脑，以及热狗形状的新型车辆。可以看到如下模式：
