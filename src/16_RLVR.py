# ## 3. GRPO 与推理模型：算法革新与实践案例
#
# 在本部分中，我们将重点探讨近期在推理模型领域（如 DeepSeek-R1）大放异彩的 **GRPO** 算法，并结合 DeepSeek、Kimi 和 Qwen 的技术报告，剖析现代强化学习从可验证奖励（RLVR）中学习的完整路径。
#
# ### 3.1 GRPO：轻量化的策略优化
# 由于 PPO 需要维护一个与策略模型同等大小的价值模型（Critic），显存开销极大。**GRPO (Group Relative Policy Optimization)** 提出了一种通过组内相对评分来消除价值模型的方案。
#
# #### GRPO 的核心思想
# GRPO 不再预测状态价值，而是针对每个问题采样一组回答（$G$ 个），利用这组回答的奖励均值和标准差来计算优势函数（Advantage）：
# $$A_i = \frac{r_i - \text{mean}(r_1, r_2, \dots, r_G)}{\text{std}(r_1, r_2, \dots, r_G)}$$
# 这种方式在保留 PPO 裁剪（Clip）特性的同时，大幅降低了训练成本。
#
# #### 算法实现
# GRPO 的实现非常直观：
# 1. 采样：旧策略对同一提示词生成多个 Rollouts。
# 2. 评分：对每个回答计算奖励。
# 3. 归一化：在组内进行 Z-Score 归一化。
# 4. 更新：直接在损失函数中引入 KL 惩罚项并进行梯度上升。
#
# ### 3.2 进阶探讨：GRPO 的偏差与长度偏见
# 虽然 GRPO 简单高效，但研究者也发现了其理论上的局限性，并提出了优化方案。
#
# #### 偏差与 Dr. GRPO
# 原始 GRPO 中除以标准差的操作实际上引入了梯度偏差。**Dr. GRPO** 提倡使用无偏的梯度估计，并修正了长度归一化项。
#
# #### 长度偏见问题
# 由于 GRPO 在目标函数中引入了长度归一化（除以 $|o_i|$），这会导致模型出现行为偏差：
# * 对于正确的回答（优势为正），模型会倾向于变得更短以获得更高更新。
# * 对于错误的回答（优势为负），由于分母大则惩罚小，模型会倾向于把错误的回答写得极长。
#
# ### 3.3 案例研究一：DeepSeek-R1 系列
# DeepSeek-R1 的成功标志着纯强化学习在复杂推理领域的突破。
#
# #### R1-Zero：纯 RL 的奇迹
# R1-Zero 直接在基础模型（Base Model）上进行 RL 训练，仅使用准确性奖励（答案是否正确）和格式奖励（是否使用了思考标签）。
# * **Aha Moment**：在训练过程中，模型自发学会了通过更长的思维链（CoT）来进行反思和重新评估。
# * **局限性**：存在可读性差、多语言混杂等问题。
#
# #### DeepSeek-R1 的四阶段配方
# 为了解决 R1-Zero 的问题，完整版的 R1 采用了更复杂的流程：
# 1. **冷启动 SFT**：使用少量高质量的长思维链数据进行初始化，解决 RL 初期不稳定的问题。
# 2. **推理 RL**：大规模 GRPO 训练，加入语言一致性奖励，减少语种混杂。
# 3. **拒绝采样与二次 SFT**：收集 RL 产生的优秀样本，结合通用数据再次微调。
# 4. **全场景 RL**：最后阶段的强化学习，兼顾推理能力和人类偏好。
#
# #### 蒸馏（Distillation）
# R1 的一个重要结论是：通过在大模型（如 671B）上运行 RL 产生的推理痕迹，可以直接蒸馏给小模型（如 Qwen-7B、Llama-8B），使其展现出极强的推理性能，甚至超越了在小模型上直接做 RL 的效果。
#
# ### 3.4 案例研究二：Kimi K1.5
# Kimi K1.5 在 R1 发布同期也展示了类似的强化学习策略，并提供了更多系统实现细节。
#
# #### 数据策略与长 CoT
# Kimi 采用了自动化的难度筛选系统，仅保留模型在 Best-of-8 采样中失败的题目进行训练，从而确保存量数据始终处于模型的“学习区”。
#
# #### RL 架构与 Partial Rollout
# 在工程实现上，Kimi 使用了 Megatron（训练）与 vLLM（推理）的混合架构：
# * **共享内存**：通过 Checkpoint Engine 快速在训练与推理模式间切换权重。
# * **部分采样（Partial Rollout）**：针对长思维链导致的批次不均匀问题，引入重放缓存（Replay Buffer）和截断采样，极大地提高了算力利用率。
#
# ### 3.5 案例研究三：Qwen 3
# Qwen 3 展示了在极少量 RL 数据下（约 4000 个样本）依然能通过 GRPO 获得显著提升。
#
# #### 思维模式融合（Thinking Mode Fusion）
# Qwen 3 引入了显式的标签来控制模型进入“思维模式”或“通用模式”。
# * **预算控制**：通过特殊的终止符，可以在测试时根据 Token 预算强制模型结束思考并给出答案。
# * **推理与通用的权衡**：实验观察到，过度的通用 RLHF 可能会轻微损害模型的数学和 STEM 推理能力。
#
# ### 3.6 总结：RLVR 的制胜法则
# 从目前的成功案例来看，构建顶尖推理模型的共性在于：
# 1. **可验证奖励**：在数学、代码等领域，使用确定性的结果反馈比使用奖励模型（RM）更鲁棒。
# 2. **简单的 RL 算法**：GRPO 或带基准的策略梯度算法已足够强大，关键在于数据的质量和规模。
# 3. **计算量缩放**：无论是训练时的 RL 迭代，还是推理时的思维链增长，都是提升模型智能的关键。

