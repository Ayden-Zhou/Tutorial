# ### 2.2 PPO 在语言模型中的理想化流程
#
# 在 LLM 场景下，PPO 的架构涉及多个模型协同工作：
#
# * **SFT 模型/参考模型**：提供基准分布，用于计算 KL 散度约束。
# * **奖励模型 (RM)**：对完整的序列进行打分。
# * **价值模型 (Value Model/Critic)**：预测状态价值，辅助计算优势函数。
# * **GAE (广义优势估计)**：结合奖励和价值模型预测，减少优势估计的方差。
#
# ### 2.3 实践中的 PPO：实现细节与挑战
#
# #### 代码级优化的重要性
# 研究表明，PPO 的成功很大程度上归功于其“代码级优化”，而不仅仅是核心算法本身。这包括：
# * **优势函数归一化 (Whitening)**：稳定梯度更新。
# * **梯度裁剪 (Gradient Clipping)**：防止梯度爆炸。
# * **奖励塑形 (Reward Shaping)**：在 token 级别添加 KL 惩罚，在最后一个 token 添加完整的奖励分值。
#
# #### PPO 的局限性
# 尽管性能强大，但 PPO 在生产环境中面临显著挑战：
# * **实现极其复杂**：需要维护多个模型副本，调试难度大。
# * **内存开销巨大**：价值模型（Value Model）通常与策略模型一样大，消耗大量显存。
# * **训练不稳定性**：对超参数、数据分布和系统实现非常敏感。
#
# ### 2.4 关键观察：过度优化 (Overoptimization)
#
# 在 RLHF 过程中需要警惕“奖励过拟合”现象。
# * **奖励曲线 vs 真实质量**：随着 KL 散度的增加，奖励模型的分数可能持续上升，但在某一时刻后，人类评估的胜率（真实质量）反而会下降。
# * **模式崩溃 (Mode Collapse)**：RLHF 可能导致模型失去概率分布的特征，熵值降低，甚至出现校准失效。

