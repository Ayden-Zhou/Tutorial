# ## 1. 强化学习与可验证奖励 (Reinforcement Learning from Verifiable Rewards)
#
# 本章节主要讨论了在语言模型后训练（Post-training）阶段，如何通过强化学习（RL）进一步提升模型性能，特别是从传统的 RLHF 演进到基于可验证奖励的 RLVR。
#
# ### 1.1 从 RLHF 到 DPO：简化的尝试
#
# 在深入 RLVR 之前，我们先回顾了为了简化经典 PPO 流程而出现的 DPO（直接偏好优化）技术。
#
# * **DPO 的核心目标**：省去奖励模型（Reward Model）的显式训练，移除在线 RL 的复杂环节（如 Rollouts 和外部循环）。
# * **方法论**：
#     * 通过对“好”的样本进行梯度上升（增加似然）。
#     * 通过对“坏”的样本进行梯度下降（减少似然），并施加适当权重。
# * **数学推导基础**：DPO 基于非参数化假设，从 RLHF 的目标函数中推导出“隐含奖励（Implied Reward）”。通过将奖励项参数化为策略本身，将 RL 问题转化为在成对偏好数据上的监督学习损失函数。
# * **实验结论**：在 AlpacaFarm 等基准测试中，DPO 能达到与 PPO 相当的性能，但训练过程更加稳定、简单。目前大多数顶尖的开源 RLHF 模型都采用了 DPO 或其变体。
#
# ### 1.2 DPO 的变体与局限性
#
# 随着技术发展，出现了一些重要的 DPO 变体：
#
# * **SimPO**：去掉了参考模型（Reference Model），通过长度归一化来减少计算开销并提升效果。
# * **长度归一化 DPO**：专门解决模型倾向于生成过长回复的偏差问题。
#
# 然而，DPO 依然存在局限：它主要处理离线、成对的偏好数据，而无法直接处理非成对或需要动态探索的任务。
#
#
#
# ### 1.3 PPO 在语言模型中的实践
#
# 尽管 DPO 很流行，但 PPO 依然是追求极致性能时的重要工具。
#
# #### PPO 的理想架构
# 在语言模型场景下，标准的 PPO 流程包含四个核心模型：
# 1.  **Policy LM**：当前训练的策略模型。
# 2.  **Ref LM**：参考模型，用于计算 KL 散度约束，防止模型跑偏。
# 3.  **Reward Model**：给最终生成的序列打分。
# 4.  **Value Model**：预测状态价值，辅助计算优势函数（Advantage）。
#
# #### 实现中的关键技术
# * **GAE (Generalized Advantage Estimate)**：用于估计优势函数。有趣的是，在语言模型的“强盗问题（Bandit Setting）”背景下，$\gamma=1$ 和 $\lambda=1$ 的配置通常能很好地工作。
# * **奖励塑形 (Reward Shaping)**：在实践中，通常在每个 Token 上施加 KL 惩罚，并将最终的稀疏奖励加在最后一个 Token（通常是结束符）上。
# * **实现复杂性**：PPO 的性能高度依赖于代码级的优化。研究表明，PPO 的收益很大程度上来自于这些细节，而非仅仅是其核心算法。
#
#
# ### 1.4 RLHF 的挑战：过优化与众数崩溃
#
# 无论是 PPO 还是 DPO，在处理人类偏好时都面临以下风险：
#
# * **过优化 (Overoptimization)**：当过度优化奖励函数时，模型会学会“作弊”（Reward Hacking），导致在真实人类评价中的胜率反而下降。
# * **众数崩溃 (Mode Collapse)**：RL 后的模型往往不再是纯粹的概率模型，其输出的熵会剧烈下降，模型变得不再“校准（Calibrated）”。
#
# ### 1.5 迈向 RLVR (Reinforcement Learning from Verifiable Rewards)
#
# 为了解决上述问题，研究者们开始将目光转向**可验证奖励**。
#
# * **核心逻辑**：在数学、代码等领域，答案的正确性是可以通过客观规则验证的，而不是依赖于模糊且容易被 Hack 的人类偏好模型。
# * **目标**：通过 RLVR，我们可以像 AlphaGo 优化围棋胜率或 LEAN 优化形式化证明一样，在语言模型中实现性能的持续、透明扩展。
#

