# ## 4. 进阶探索与系统实现：那些没走通的路与工程细节
#
# 在本部分的最后，我们将深入探讨在追求最强推理模型过程中，研究者们尝试过但由于各种原因未能成为主流的方案，以及在 Kimi 和 Qwen 实践中沉淀下来的关键工程经验与系统架构。
#
# ### 4.1 那些“失败”的尝试：PRM 与 MCTS
#
# 在 DeepSeek-R1 的开发过程中，研究团队曾对目前学界热门的 PRM（过程奖励模型）和 MCTS（蒙特卡洛树搜索）进行了大规模实验，但最终在 R1 的正式配方中舍弃了它们。
#
# #### 过程奖励模型 (PRM) 的局限性
# 尽管 PRM 理论上可以引导模型更细致地进行每一步推理，但在实践中存在三个核心限制：
# * **步骤定义难**：在通用的推理任务中，很难明确界定什么才是一个“细粒度的步骤”。
# * **标注成本与质量**：无论是模型自动标注还是人工标注，在大规模扩展时都难以保证准确性。
# * **奖励作弊 (Reward Hacking)**：引入模型驱动的 PRM 后，策略模型极易通过“钻空子”来获得高分，而不是真正提升推理能力，且重新训练奖励模型会使流水线变得异常复杂。
#
# #### 蒙特卡洛树搜索 (MCTS) 的挑战
# 受 AlphaGo 启发，MCTS 被认为可以提升测试时的计算扩展性（Test-time scaling），但将其应用于语言模型时遇到了困难：
# * **搜索空间巨大**：不同于棋类运动，Token 生成的搜索空间几乎是无限的。
# * **计算开销极高**：在推理过程中进行深度搜索会引入极大的计算负载，相比之下，单纯增加 CoT 长度的性价比更高。
#
# ### 4.2 Kimi K1.5 的工程实践：系统与奖励优化
#
# Kimi Team 在其技术报告中分享了如何通过精细的工程设计，让 RL 过程更高效、更受控。
#
# #### 奖励模型的阶跃
# Kimi 发现传统的奖励模型（Classic RM）准确率仅为 84.4% 左右，不足以支撑高质量的 RL 训练。通过使用 800k 样本训练专门的 **Chain-of-Thought RM** 进行答案等价性检查，其准确率跃升至 98.5%，确保了反馈的准确性。
#
# #### 动态长度惩罚
# 为了解决 CoT 随训练过程无限膨胀的问题，Kimi 引入了长度奖励机制：
# * 对于**正确**的回答，模型会被引导向更短、更简洁的路径。
# * 对于**错误**的回答，则惩罚其冗余。
# 这种机制确保了模型在保持推理能力的同时，不会产生过多的废话 Token。
#
# #### 混合部署框架
# 在系统底层，Kimi 采用了 **Megatron (训练) + vLLM (推理) + Mooncake** 的混合架构：
# * **显存动态切换**：通过 Checkpoint Engine 在训练（Megatron）和采样（vLLM）阶段之间快速切换权重并动态管理 GPU 显存。
# * **Partial Rollout (部分采样)**：通过维护一个重放缓冲区（Replay Buffer），系统可以截断过长的采样序列并重复利用部分中间结果，解决了长 CoT 导致的 Batch 负载不均问题，大幅提升了算力利用率。
#
# ### 4.3 Qwen 3 的创新：思维模式融合与预算控制
#
# Qwen 3 则在如何平衡“推理”与“通用”能力上给出了新的思路。
#
# #### 思维模式融合 (Thinking Mode Fusion)
# Qwen 3 不再让模型在所有场景下都强制进行长思考，而是通过显式的标签（如 `/think` 或 `/no_think`）来切换模式。
# * **数据混合**：在 SFT 和 RL 阶段，将带有思考路径的数据与直接输出结果的数据按比例混合。
# * **模式可控**：用户可以通过指令决定模型是否开启推理模式。
#
# #### 推理预算控制 (Budget Control)
# 这是一种在测试阶段控制计算成本的策略。当模型的思考长度达到用户定义的阈值时，系统会自动插入特定的终止指令（如“考虑到时间限制，我将直接给出结论”），强制模型结束思考。实验表明，这种能力在经过“模式融合”训练后可以自然涌现。
#
# ### 4.4 总结：RLVR 时代的启示
#
# 通过对本课程中 PPO、DPO 到 GRPO 的学习，我们可以总结出当前语言建模领域关于强化学习的几点共识：
#
# #### 过优化是永恒的挑战
# 无论采用哪种算法，盲目追求奖励分数都会导致模型“坏掉”（过拟合或校准失效）。在窄域推理任务（如数学、代码）中使用**可验证奖励 (RLVR)** 是目前规避该问题的最稳妥方案。
#
# #### 简单胜过复杂
# DeepSeek-R1 的成功证明了，相比于复杂的 PRM 或 MCTS，使用简单的 **GRPO** 算法结合高质量的冷启动数据和大规模计算量缩放，就能产生惊人的推理能力。
#
# #### 能力的博弈
# 通用能力的 RLHF 往往会与特定领域的推理能力（STEM/Math）产生细微的冲突。现代模型正通过多阶段训练（Reasoning RL -> General RL）和模式切换技术，努力在“深度思考”和“通用对话”之间寻找最佳平衡点。
